export function normalizeTopicRecommendations(genre: TopicGenre, value: unknown): TopicRecommendation[] {
  if (!Array.isArray(value)) return [];
  const allowedStyles = getTopicStyles(genre);
  const defaultStyle = getDefaultTopicStyle(genre);

  return value
    .map((raw): TopicRecommendation | null => {
      if (!isRecord(raw)) return null;
      const title = String(raw.title || "").trim();
      const summary = String(raw.summary || "").trim();
      const outline = String(raw.outline || "").trim();
      const requestedStyle = String(raw.style || "").trim();
      const requestedGenre = String(raw.genre || "").trim();
      if (!title || !summary || !outline) return null;
      if (requestedGenre !== genre) return null;
      if (requestedStyle && !allowedStyles.includes(requestedStyle)) return null;
      const tags = Array.isArray(raw.tags)
        ? raw.tags.map((tag) => String(tag).trim()).filter(Boolean).slice(0, 5)
        : [];
      return {
        title,
        summary,
        outline,
        tags,
        style: requestedStyle || defaultStyle,
        genre,
      };
    })
    .filter((item): item is TopicRecommendation => item !== null);
}

function topic(
  title: string,
  summary: string,
  outline: string,
  tags: string[],
  style: string,
): LocalTopicSeed {
  return { title, summary, outline, tags, style };
}

function isRecord(value: unknown): value is Record<string, unknown> {
  return typeof value === "object" && value !== null;
}
